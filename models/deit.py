class ConvMlp(nn.Module):
    """ MLP as used in Vision Transformer, MLP-Mixer and related networks
    """

    def __init__(
            self,
            in_features,
            hidden_features=None,
            out_features=None,
            act_layer=nn.GELU,
            norm_layer=None,
            bias=True,
            drop=0.,
            use_conv=False,
    ):
        super().__init__()
        out_features = out_features or in_features
        hidden_features = hidden_features or in_features
        bias = to_2tuple(bias)
        drop_probs = to_2tuple(drop)
        self.fc1 = nn.Linear(in_features, hidden_features, bias=bias[0])
        self.fc2 = nn.Linear(hidden_features, out_features, bias=bias[1])
        self.fc1a = nn.Conv1d(in_features, hidden_features//2, bias=bias[0], kernel_size=198)
        self.fc1b = nn.Conv1d(in_features, hidden_features//2, bias=bias[0], kernel_size=198)
        print(self.fc1.weight.shape)
        self.fc1a.weight = torch.nn.Parameter(self.fc1.weight[:hidden_features//2, :].view(hidden_features//2, in_features, 1))
        self.fc1a.bias = torch.nn.Parameter(self.fc1.bias[:hidden_features//2].view(hidden_features//2))
        self.fc1b.weight = torch.nn.Parameter(self.fc1.weight[hidden_features//2:, :].view(hidden_features//2, in_features, 1))
        self.fc1b.bias = torch.nn.Parameter(self.fc1.bias[hidden_features//2:].view(hidden_features//2))
        print(self.fc1b.weight.shape)

        self.act = act_layer()
        self.drop1 = nn.Dropout(drop_probs[0])
        self.norm = norm_layer(hidden_features) if norm_layer is not None else nn.Identity()
        self.fc2a = nn.Conv1d(hidden_features, in_features//2, bias=bias[1], kernel_size=198)
        self.fc2b = nn.Conv1d(hidden_features, in_features//2, bias=bias[1], kernel_size=198)
        self.fc2a.weight = torch.nn.Parameter(self.fc2.weight[:in_features//2, :].view(in_features//2, hidden_features, 1))
        self.fc2a.bias = torch.nn.Parameter(self.fc2.bias[:in_features//2].view(in_features//2))
        self.fc2b.weight = torch.nn.Parameter(self.fc2.weight[in_features//2:, :].view(in_features//2, hidden_features, 1))
        self.fc2b.bias = torch.nn.Parameter(self.fc2.bias[in_features//2:].view(in_features//2))

        self.drop2 = nn.Dropout(drop_probs[1])

    def forward(self, x):
        xa, xb = self.fc1a(torch.transpose(x, 1, 2)), self.fc1b(torch.transpose(x, 1, 2))
        xa, xb = self.act(xa), self.act(xb)
        x = torch.concat((xa, xb), axis=1)
        x = self.drop1(x)
        x = self.norm(x)
        xa, xb = self.fc2a(x), self.fc2b(x)
        x = torch.concat((xa, xb), axis=1)
        x = torch.transpose(x, 1, 2)
        x = self.drop2(x)

        return x
class ModifiedAttention(nn.Module):
    fused_attn: Final[bool]

    def __init__(
            self,
            dim: int,
            num_heads: int = 8,
            qkv_bias: bool = False,
            qk_norm: bool = False,
            attn_drop: float = 0.,
            proj_drop: float = 0.,
            norm_layer: nn.Module = nn.LayerNorm,
    ) -> None:
        super().__init__()
        assert dim % num_heads == 0, 'dim should be divisible by num_heads'
        self.num_heads = num_heads
        self.head_dim = dim // num_heads
        self.scale = self.head_dim ** -0.5
        self.fused_attn = 0

        self.qkv = nn.Linear(dim, dim * 3, bias=qkv_bias)
        self.q_norm = norm_layer(self.head_dim) if qk_norm else nn.Identity()
        self.k_norm = norm_layer(self.head_dim) if qk_norm else nn.Identity()
        self.attn_drop = nn.Dropout(attn_drop)
        self.proj = nn.Linear(dim, dim)
        self.proj_drop = nn.Dropout(proj_drop)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        B, N, C = x.shape
        qkv = self.qkv(x)

        q,k,v = torch.chunk(qkv, 3, dim=-1)
        # Reshape and transpose
        q = q.reshape(B, N, self.num_heads, self.head_dim).permute(0, 2, 1, 3)
        k = k.reshape(B, N, self.num_heads, self.head_dim).permute(0, 2, 1, 3)
        v = v.reshape(B, N, self.num_heads, self.head_dim).permute(0, 2, 1, 3)

        q, k = self.q_norm(q), self.k_norm(k)

        if self.fused_attn:
            x = F.scaled_dot_product_attention(
                q, k, v,
                dropout_p=self.attn_drop.p if self.training else 0.,
            )
        else:
            q = q * self.scale
            k = k.transpose(-2, -1)
            qc = torch.chunk(q, 3, dim=1)
            kc = torch.chunk(k, 3, dim=1)

            attn = torch.concat([qc[i] @ kc[i] for i in range(3)], axis=1)

            attn = attn.softmax(dim=-1)
            attn = self.attn_drop(attn)
            attn = torch.chunk(attn, 3, dim=1)
            v = torch.chunk(v, 3, dim=1)
            x = torch.concat([attn[i] @ v[i] for i in range(3)], axis=1)

        x = x.transpose(1, 2).reshape(B, N, C)
        x = self.proj(x)
        x = self.proj_drop(x)
        return x




class ModifiedBlock(nn.Module):
    def __init__(
            self,
            dim: int,
            num_heads: int,
            mlp_ratio: float = 4.,
            qkv_bias: bool = False,
            qk_norm: bool = False,
            proj_drop: float = 0.,
            attn_drop: float = 0.,
            init_values: Optional[float] = None,
            drop_path: float = 0.,
            act_layer: nn.Module = nn.GELU,
            norm_layer: nn.Module = nn.LayerNorm,
            mlp_layer: nn.Module = ConvMlp,
    ) -> None:
        super().__init__()
        self.norm1 = norm_layer(dim)
        self.attn = ModifiedAttention(
            dim,
            num_heads=num_heads,
            qkv_bias=qkv_bias,
            qk_norm=qk_norm,
            attn_drop=attn_drop,
            proj_drop=proj_drop,
            norm_layer=norm_layer,
        )
        self.ls1 = LayerScale(dim, init_values=init_values) if init_values else nn.Identity()
        self.drop_path1 = DropPath(drop_path) if drop_path > 0. else nn.Identity()

        self.norm2 = norm_layer(dim)
        self.mlp = mlp_layer(
            in_features=dim,
            hidden_features=int(dim * mlp_ratio),
            act_layer=act_layer,
            drop=proj_drop,
        )
        self.ls2 = LayerScale(dim, init_values=init_values) if init_values else nn.Identity()
        self.drop_path2 = DropPath(drop_path) if drop_path > 0. else nn.Identity()

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = x + self.drop_path1(self.ls1(self.attn(self.norm1(x))))
        x = x + self.drop_path2(self.ls2(self.mlp(self.norm2(x))))
        return x


class DistilledVisionTransformer(VisionTransformer):
    def __init__(self, *args, **kwargs):
        super().__init__(mlp_layer=ConvMlp, block_fn=ModifiedBlock, *args, **kwargs)
        self.dist_token = nn.Parameter(torch.zeros(1, 1, self.embed_dim))
        num_patches = self.patch_embed.num_patches
        self.pos_embed = nn.Parameter(torch.zeros(1, num_patches + 2, self.embed_dim))
        self.head_dist = nn.Linear(self.embed_dim, self.num_classes) if self.num_classes > 0 else nn.Identity()

        trunc_normal_(self.dist_token, std=.02)
        trunc_normal_(self.pos_embed, std=.02)
        self.head_dist.apply(self._init_weights)

    def forward_features(self, x):
        # taken from https://github.com/rwightman/pytorch-image-models/blob/master/timm/models/vision_transformer.py
        # with slight modifications to add the dist_token
        B = x.shape[0]
        x = self.patch_embed(x)

        cls_tokens = self.cls_token.expand(B, -1, -1)  # stole cls_tokens impl from Phil Wang, thanks
        dist_token = self.dist_token.expand(B, -1, -1)
        x = torch.cat((cls_tokens, dist_token, x), dim=1)

        x = x + self.pos_embed
        x = self.pos_drop(x)

        for blk in self.blocks:
            x = blk(x)

        x = self.norm(x)
        return x

    def forward(self, x):
        x = self.forward_features(x)
        x = self.head(x)
